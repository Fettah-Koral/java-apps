
public class ForDongusu {
    public static void main(String[] args) {
       for (int sayac = 3 ; sayac <= 10 ; sayac += 2){
           System.out.println(sayac);
       }

        System.out.println("----------------------------");

       int k=3;
       while (k<=10){
           System.out.println(k);
           k+=2;
       }

    }
}
